	private UniquePrime calc = new UniquePrime();;

	@Test
	public void test_min_long_computes_correct_limit() throws Exception {
		for (int i = 1; i < 18; i++) {
			long minLong = calc.minLong(i);
			int strLen = new Long(minLong).toString().length();
			System.out.println(i + " min: " + minLong);
			assertEquals(i, strLen);
		}
	}

	@Test
	public void test_max_long_computes_correct_limit() throws Exception {
		for (int i = 1; i < 18; i++) {
			long maxLong = calc.maxLong(i);
			int strLen = new Long(maxLong).toString().length();
			System.out.println(i + " max: " + maxLong);
			assertEquals(i, strLen);
		}
	}

	@Test
	public void test_18_is_ok() throws Exception {
		long for18 = calc.minLong(18);
		assertEquals(18, new Long(for18).toString().length());
	}

	@Test(expected = IllegalArgumentException.class)
	public void test_19_is_to_many() throws Exception {
		calc.minLong(19);
	}

	@Test
	public void test_a_number_is_computed_for_5() throws Exception {
		int n = 5;
		long start = System.currentTimeMillis();
		long actual = calc.findNumber(n);
		System.out.println("Found " + actual +" for "+n+" in " + (System.currentTimeMillis() - start) + "ms");
		assertEquals(n, new Long(actual).toString().length());
	}
